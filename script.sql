CREATE TABLE LIBRO (
    ID INTEGER GENERATED ALWAYS AS IDENTITY (START WITH 1, INCREMENT BY 1),
    NOMBRE VARCHAR(50) NOT NULL,
    AUTOR VARCHAR(50) NOT NULL,
    UBICACION SMALLINT NOT NULL,
    PRIMARY KEY(ID),
    FOREIGN KEY(UBICACION) REFERENCES ESTANTERIA
);

